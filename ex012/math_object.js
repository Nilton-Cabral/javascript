// using math object in JavaScript

let = number = 3;
console.log('10 / 3 =', 10 / number)
// using ceil method to round a number, the ceil method round up a number to a nearest whole number
console.log('10 / 3 =', Math.ceil(10 / number), 'Math.ceil');


// using floor method to round a number, this method round a number down a number to a nearest whole number
console.log('10 / 3 =', Math.floor(10 / number), 'Math.floor');

// using round method, round up if the decimal part is greater or equal to 5
// and round down if the decimal part is less than 5
console.log('10 / 3 =', Math.round(10 / number), 'Math.round');


// square root
console.log('square root of (4) is ', Math.sqrt(4));

// cube root
console.log('cube root of (8) is ', Math.cbrt(8));

// absolute number 
console.log('absolute(-5) is', Math.abs(-5));

// pow (potency)
console.log('(8) raised to (2) is', Math.pow(8, 2));

// getting the max value of a set
console.log('[1,2,3,8] the max number is', Math.max(1,2,3,8));

// getting the min value of a set
console.log('[-64,0,1,2,3,8] the min number is', Math.min(-64,0,1,2,3,8));

