// strings can be enclosed in double quotes "", single quotes'', and within two serious accents ``

// double quotes
let doublequotes = "JavaScript";
console.log(doublequotes);

// single quotes
let singlequotes = 'JavaScript';
console.log(singlequotes);

// serious accents
let graveaccent = `JavaScript`;
console.log(graveaccent);

// comparing the above strings

console.log(doublequotes === singlequotes);
console.log(doublequotes === graveaccent);
console.log(singlequotes === graveaccent);