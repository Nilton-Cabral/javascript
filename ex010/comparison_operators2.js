// continuing with comparison operators

// greater than '>'
console.log("greater than '>'")
console.log('2 > 3', 2 > 3);
console.log('-1 > -2', -1 > -2);
console.log('10 > 5', 10 > 5);

// greater or equal than '>='
console.log("greater or equal than '>='")
console.log('7 >= 7', 7 >= 7);
console.log('12 >= 10', 12 >= 10);
console.log('12.1 >= 12.01', 12.1 >= 12.01);
console.log('5 >= 4.99', 5 >= 4.99);

// less than '<'
console.log("less than '<'");
console.log('1 < "1"', 1 < '1');
console.log('0.5 < true', 0.5 < true);
console.log('-5 < -5.0', -5 < -5.0);

// less or equal than
console.log("less or equal than '<='");
console.log('1 <= "1"', 1 <= '1');
console.log('-5 <= 5', -5 <= 5);
console.log('-1 <= false', -1 <= false);
