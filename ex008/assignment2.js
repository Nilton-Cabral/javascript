//pre-incrememt and post-increment


//definig a variable
let number = 5;

//post-icrement addition
console.log('post-icrement addition')
console.log(`value of number ${number}`);
console.log(`(number++) result ${number++}`);
console.log(`value of number ${number}`);

//redefinig the value of number
number = 5;

//pre-increment addition
console.log('pre-increment addition')
console.log(`value of number ${number}`);
console.log(`(++number) result ${++number}`);
//redefinig the value of number
number = 5;


//pre-decrememt and post-decrement

//post-decrement
console.log('post-decrement')
console.log(`value of number ${number}`);
console.log(`(number--) result ${number--}`);
console.log(`value of number ${number}`);
//redefinig the value of number
number = 5;

//pre-decrement
console.log('pre-decrement')
console.log(`value of number ${number}`);
console.log(`(--number) result ${--number}`);
//redefinig the value of number
number = 5;

